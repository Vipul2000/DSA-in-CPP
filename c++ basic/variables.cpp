#include<iostream>
#include<bits/stdc++.h>
using namespace std;

int main(){
     //integer data types
      
      int apples = 5;

      cout<<apples<<endl;
      cout <<"size of apples is "<<sizeof(apples)<<endl;

     //char data type
      char sign = 'a';
      cout<<sign<<endl;
      cout<<"size of char is "<< sizeof(sign)<<endl;

     //float data type
      float score = 3.14;
      cout<<score<<endl;
      cout<<"size of char is "<<sizeof(score)<<endl;

     //boolean data type
      bool flag = true;
      cout<<flag<<endl;
      cout<<"size of bool is "<<sizeof(flag)<<endl;
     return 0;
}


